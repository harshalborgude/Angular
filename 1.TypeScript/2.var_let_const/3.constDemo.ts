// 'const' keyword :
/*
    - to declare constant , we can use const keyword.
    - whenever we declare 'const' , initializing value is important.
    - we can't modify the value for 'const' variables in JS/TS.
    
    - initialization of const variable is mandatory , cant initialize that later.
    - it can't be modied once it's initialized.

*/

//---------------------------------------------------------

const myName:string = "harshal";
// myName="modified";     // not allowed to modify const value
console.log("myName :"+myName);

